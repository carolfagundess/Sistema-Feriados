    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        //vai chamar o metodo calcular dias uteis
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        ArrayList<Feriado> listaFeriados = new ArrayList<>();
        FeriadoTableModel feriadoTableModel = new FeriadoTableModel(new ArrayList<>());

        jTable1.setModel(feriadoTableModel);

        if (validarForm()) {
            DiasUteis diasUteis = new DiasUteis();
            try {
                listaFeriados = diasUteis.obterFeriados(LocalDate.parse(jFormattedTxtInicial.getText(), formatter), LocalDate.parse(jFormattedTxtFinal.getText(), formatter));
            } catch (SQLException ex) {
                Logger.getLogger(IFrameCalcularDias.class.getName()).log(Level.SEVERE, null, ex);
            }
            long resulDiasUteis = diasUteis.contarDiasUteisComFeriados(LocalDate.parse(jFormattedTxtInicial.getText(), formatter), LocalDate.parse(jFormattedTxtFinal.getText(), formatter), listaFeriados);
            System.out.println("Numero de dias uteis Ã©: " + resulDiasUteis);
            txtDias.setText(String.valueOf(resulDiasUteis));
            
            feriadoTableModel = new FeriadoTableModel(listaFeriados);
            jTable1.setModel(feriadoTableModel);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        limparCampos();
        txtDias.setText("");
    }//GEN-LAST:event_btnLimparActionPerformed

    private boolean validarForm() {
        if (jFormattedTxtInicial.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Data Incial", this.getTitle(), JOptionPane.ERROR_MESSAGE);
            jFormattedTxtInicial.requestFocus();
            return false;
        }
        if (jFormattedTxtFinal.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Data Final", this.getTitle(), JOptionPane.ERROR_MESSAGE);
            jFormattedTxtFinal.requestFocus();
            return false;
        }
        return true;
    }

